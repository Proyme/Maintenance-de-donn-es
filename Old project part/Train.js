class Train{
	constructor(nom, role, description, couleur, vitesse, nombreRoue, localisation, id = null){
		this.nom = nom;
		this.role = role;
		this.description = description;
		this.couleur = couleur;
		this.vitesse = vitesse;
		this.nombreRoue = nombreRoue;
		this.localisation = localisation;
		this.id = id;
		
	}
}